def displayMenu():
    menu = ["Login", "Sign Up", "Exit"];
    center = int(120/2-getLongestMenu(menu)); # 120 is the spaces from the start of the welcome message till the end
    for i in range(0, len(menu)):
        if (i == len(menu)-1):
            print(" " * center + f"[0] " + menu[i])
        else:
            print(" " * center + f"[{i + 1}] " + menu[i])
    
def getLongestMenu(menu):
    longestMenu = len(max(menu, key=len));
    return longestMenu;

def clearScreen():
    if platform.system() == "Windows":
        os.system("cls");
    else:
        os.system("clear");
        
